int	ft_atoi(char *str)
{
	int		i;
	int		k;
	int		t;

	i = 0;
	k = 1;
	t = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			k = k * (-1);
		i++;
	}
	while (str[i] <= '9' && str[i] >= '0')
	{
		t = t * 10 + str[i] - 48;
		i++;
	}
	t = t * k;
	return (t);
}

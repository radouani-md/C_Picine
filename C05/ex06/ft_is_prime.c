int	ft_is_prime(int nb)
{
	int		i;
	int		prim;

	i = 2;
	prim = 1;
	if (nb < 2)
	{
		return (0);
	}
	while (i <= nb / 2)
	{
		if (nb % i == 0)
		{
			prim = 0;
			break ;
		}
		i++;
	}
	if (prim == 1)
		return (1);
	else
		return (0);
}
